//variáveis

int numero1 = 5;
int numero2 = 8;

//maior
Console.WriteLine(numero1 > numero2);

//maior ou igual
Console.WriteLine(numero1 >= numero2);

//menor
Console.WriteLine(numero1 < numero2);

//menor ou igual
Console.WriteLine(numero1 <= numero2);

//igual 
Console.WriteLine(numero1 == numero2);

//diferente
Console.WriteLine(numero1 != numero2);
