//variáveis
int numero1 = 30;
int numero2 = 3;

//Divisão
Console.WriteLine(numero1 / numero2);

//Multiplicação
Console.WriteLine(numero1 * numero2);

//soma
Console.WriteLine(numero1 + numero2);

//subtração
Console.WriteLine(numero1 - numero2);

//módulo(resto da divisão)
Console.WriteLine(numero1 % numero2);

/*incrementador
pré-incremento*/
numero1++;
Console.WriteLine(numero1);

/*Decrementador
pré-decremento*/
numero2--;
Console.WriteLine(numero2);
